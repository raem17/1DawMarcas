var num = prompt("Introduce un número entre 1 y 7.") / 1;

switch (num) {
case 1: alert("Lunes.");
    
    break;
case 2: alert("Martes.");
    
    break;
case 3: alert("Miércoles.");
    
    break;
case 4: alert("Jueves.");
    
    break;
case 5: alert("Viernes.");
    
    break;
case 6: alert("Sábado.");
    
    break;
case 7: alert("Domingo.");
    
    break;

default: alert("Número no válido.");
    break;
}